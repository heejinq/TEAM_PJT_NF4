package mit.c301.nf4.vo;

import java.util.Date;

public class AnnounceVO {

	private Integer a_no;
	private String a_title;
	private String a_content;
	private Integer a_view;
	private Date a_date;
	public Integer getA_no() {
		return a_no;
	}
	public void setA_no(Integer a_no) {
		this.a_no = a_no;
	}
	public String getA_title() {
		return a_title;
	}
	public void setA_title(String a_title) {
		this.a_title = a_title;
	}
	public String getA_content() {
		return a_content;
	}
	public void setA_content(String a_content) {
		this.a_content = a_content;
	}
	public Integer getA_view() {
		return a_view;
	}
	public void setA_view(Integer a_view) {
		this.a_view = a_view;
	}
	public Date getA_date() {
		return a_date;
	}
	public void setA_date(Date a_date) {
		this.a_date = a_date;
	}
	
	
}
